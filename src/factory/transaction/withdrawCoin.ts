import AccountType from '../accountType';
import { IAction as IAuthorizeWithdrawCointAccountAction } from '../action/authorize/withdraw/account/coin';
import * as MoneyTransferActionFactory from '../action/transfer/moneyTransfer';
import { IExtendId } from '../autoGenerated';
import OrganizationType from '../organizationType';
import { IAccount } from '../ownershipInfo';
import PersonType from '../personType';
import * as TransactionFactory from '../transaction';
import TransactionType from '../transactionType';

/**
 * 取引開始パラメーターインターフェース
 */
export type IStartParams = TransactionFactory.IStartParams<TransactionType.WithdrawCoin, IAgent, IRecipient, IObject>;
export interface IRecipient {
    typeOf: OrganizationType | PersonType;
    id?: string;
    name: string;
    url?: string;
}
export interface IAgent {
    typeOf: OrganizationType | PersonType;
    id?: string;
    name: string;
    url?: string;
}
export type IResult = any;
/**
 * エラーインターフェース
 */
export type IError = any;
/**
 * 入金先のロケーションインターフェース
 */
export type ILocation = IAccount<AccountType.Coin>;
export type IAuthorizeAction = IAuthorizeWithdrawCointAccountAction;
/**
 * 取引対象物インターフェース
 */
export interface IObject {
    amount: number;
    fromLocation: ILocation;
    notes: string;
    authorizeActions: IAuthorizeAction[];
}
export interface IPotentialActions {
    moneyTransfer: MoneyTransferActionFactory.IAttributes;
}
/**
 * コイン出金取引属性インターフェース
 */
export interface IAttributes extends TransactionFactory.IAttributes<IStartParams, IResult, IError, IPotentialActions> {
}
/**
 * コイン出金取引インターフェース
 */
export type ITransaction = IExtendId<IAttributes>;
